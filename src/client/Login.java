    private void jButton_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LoginActionPerformed
        String cpf = jTextField_Username.getText();
        String password = String.valueOf(jPasswordField.getPassword());

        PersonData auth = new PersonData();

        int perfil = auth.loginAutentication(cpf, password);

        if(perfil == 1){
            AdminOptions adm = new AdminOptions();
            this.dispose();
            adm.setVisible(true);
            return;
        }
        if(perfil == 2){
            AttOptions att = new AttOptions();
            this.dispose();
            att.setVisible(true);
            return;
        }
        JOptionPane.showMessageDialog(null,"Senha ou Cpf invalidos");
        return;
    }//GEN-LAST:event_jButton_LoginActionPerformed

