    public void jButton_CadastrarActionPerformed(java.awt.event.ActionEvent evt) throws SQLException, IOException {
        PersonData insert = new PersonData();
        Password pass = new Password();

        String cpf = jTextField_CPF.getText();
        String name = jTextField_Nome.getText();
        int age = Integer.parseInt(jTextField_Idade.getText());
        String email = jTextField_Email.getText();
        String address = jTextField_Logradouro.getText();
        int addressNumber =  Integer.parseInt(jTextField_Numero.getText());
        String district = jTextField_Bairro.getText();
        String city = jTextField_Cidade.getText();
        String state = jTextField_Estado.getText();
        int cep = Integer.parseInt(jTextField_CEP.getText());
        boolean healthPosition = true;
        int priority = 3;

        if (jRadioButton_healthPosition2.isSelected()) {
            healthPosition = false;
        }
        int perfil = jComboBox_Usuario.getSelectedIndex()+1;
        if (perfil == 3){
            if(healthPosition){
                priority = 2;
            }
            if(age>=70){
                priority = 1;
            }
            Person p = new Person( name,  cpf,  age,  email, address,  addressNumber,  state,  city,  district,  cep,  healthPosition,  priority);
            insert.insertPerson(p);
            return;
        }
        String password = pass.createPassword(email);

        Person p = new Person(name,  cpf, age,  email,  password, address,
        addressNumber,  state,  city,  district,  cep,
         perfil,  healthPosition,  priority);

        insert.insertPerson(p);
        return;
    }

