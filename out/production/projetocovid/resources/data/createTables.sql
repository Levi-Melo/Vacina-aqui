USE PROJETO_COVID;

CREATE TABLE PERSON_PROFILE(
	ID INT NOT NULL AUTO_INCREMENT,	
	CODIGO VARCHAR(6),
    DESCRICAO VARCHAR(24),
	PRIMARY KEY (ID)
);

CREATE TABLE PEOPLE (
	ID INT NOT NULL AUTO_INCREMENT,
	CPF VARCHAR(11) ,
    NOME VARCHAR(30) ,
    IDADE SMALLINT ,
    EMAIL VARCHAR(30),
    PASSWORD VARCHAR(8),
    ENDERECO VARCHAR(100) ,
    DATA_DE_VACINACAO DATE,
    CARGO_AREA_PUBLICA BIT(1), 
    ID_PERFIL INT,
    NIVEL_DE_PRIORIDADE INT,
    PRIMARY KEY (ID),
    FOREIGN KEY (ID_PERFIL) REFERENCES PERSON_PROFILE(ID)
);
    

select * from PEOPLE where DATA_DE_VACINACAO between 20000406 and curdate()
    